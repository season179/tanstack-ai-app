type JsonSchemaProperty = Record<string, unknown>;

export type RealisticToolInput = Record<string, unknown>;

export type RealisticToolOutput = {
  status: "mocked";
  toolName: string;
  service: string;
  action: string;
  summary: string;
  receivedInput: RealisticToolInput;
  records: Array<{
    id: string;
    label: string;
    url?: string;
  }>;
};

export type RealisticToolSpec = {
  name: string;
  title: string;
  service: string;
  action: string;
  description: string;
  properties: Record<string, JsonSchemaProperty>;
  required?: string[];
};

const str = (description: string, extra: JsonSchemaProperty = {}): JsonSchemaProperty => ({
  type: "string",
  description,
  ...extra,
});

const int = (description: string, extra: JsonSchemaProperty = {}): JsonSchemaProperty => ({
  type: "integer",
  description,
  ...extra,
});

const num = (description: string, extra: JsonSchemaProperty = {}): JsonSchemaProperty => ({
  type: "number",
  description,
  ...extra,
});

const bool = (description: string): JsonSchemaProperty => ({
  type: "boolean",
  description,
});

const arr = (
  description: string,
  items: JsonSchemaProperty = { type: "string" },
  extra: JsonSchemaProperty = {},
): JsonSchemaProperty => ({
  type: "array",
  description,
  items,
  ...extra,
});

const obj = (
  description: string,
  properties: Record<string, JsonSchemaProperty>,
  required: string[] = [],
): JsonSchemaProperty => ({
  type: "object",
  description,
  properties,
  required,
  additionalProperties: false,
});

const mapObj = (description: string, valueSchema: JsonSchemaProperty = {}): JsonSchemaProperty => ({
  type: "object",
  description,
  additionalProperties: valueSchema,
});

const enumStr = (description: string, values: string[]): JsonSchemaProperty =>
  str(description, { enum: values });

